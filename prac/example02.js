function sum(p1, p2) {
  console.log(p1 + p2);
}

const minus = (p1, p2) => {
  console.log(p1 - p2);
};

//arrow function 으로 곱하기 나누기 구현하기
const multi = (p1, p2) => {
  console.log(p1 * p2);
};

const div = (p1, p2) => {
  console.log(p1 / p2);
};

sum(4, 2);
minus(5, 1);
minus(3, 2);
multi(3, 10);
div(100, 5);
